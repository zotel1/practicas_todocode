public class EjemploForSecuencia {

    // Realizar un programa que muestre por pantalla los numeros del 200 a 250 saltando de 2 en 2
    public static void main(String[] args) {

        int cont = 200;
        while (cont <= 250) {
            System.out.println(cont );
            cont += 2;
        }

    }
}
